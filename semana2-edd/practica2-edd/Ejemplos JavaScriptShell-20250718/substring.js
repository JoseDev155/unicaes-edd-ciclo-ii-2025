// Definir una cadena de texto
let texto = "JavaScript es divertido";

// Usar String.substring() para obtener una parte de la cadena
let subcadena1 = texto.substring(0, 10);  // Extraer desde el índice 0 hasta el índice 10
console.log("Subcadena 1 (índice 0 a 10):", subcadena1);

let subcadena2 = texto.substring(11);  // Extraer desde el índice 11 hasta el final
console.log("Subcadena 2 (índice 11 hasta el final):", subcadena2);

let subcadena3 = texto.substring(4, 15);  // Extraer desde el índice 4 hasta el índice 15
console.log("Subcadena 3 (índice 4 a 15):", subcadena3);
