// Definir un array de frutas
let frutas = ["Manzana", "Banana", "Naranja"];

// Mostrar el array original
console.log("Array original:", frutas);

// Usar Array.push() para agregar una nueva fruta al final del array
frutas.push("Mango");

// Mostrar el array después de agregar el mango
console.log("Array después de agregar 'Mango':", frutas);

// Agregar más elementos al array
frutas.push("Uva", "Piña");

// Mostrar el array después de agregar más frutas
console.log("Array después de agregar 'Uva' y 'Piña':", frutas);

// Mostrar el número total de elementos en el array
console.log("Número total de frutas:", frutas.length);
